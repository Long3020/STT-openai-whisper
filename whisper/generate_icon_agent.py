def find_overlaps(i, overlays):
    start_i = overlays[i][1]
    end_i = overlays[i][2]
    overlaps = []
    for j, (path, start, end) in enumerate(overlays):
        # Kiá»ƒm tra overlap thá»i gian
        if not (end <= start_i or end_i <= start):
            overlaps.append(j)
    return overlaps

